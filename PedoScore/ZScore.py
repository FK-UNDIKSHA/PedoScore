def IMT(weight:float, height:float) -> dict:
    print(weight)
    print(height)
    imt = float(weight/(height*height))
    print(imt)
    stats_ = ""

    if imt < 18.5:
        stats_ = "Suspect Stunting" #"Underweight"
    elif imt >= 18.5 and imt <= 24.9:
        stats_ = "Non Suspect Stunting" #"Normal"
    elif imt >25.0 and imt <=29.9:
        stats_ = "Non Suspect Stunting" #"Pre-Obese"
    elif imt >= 30.0:
        stats_ = "Non Suspect Stunting" #"Obese"

    return {"IMT": imt, "Status": stats_}

def ZScore(age: int, weight: float, gender: str) -> dict:
    #0, 11, M

    if gender.upper() == "M":
        #Calculating SD based on given constant
        if weight == M_dict[age]["Berat"][3]:
            SD = (weight-M_dict[age]["Berat"][3])/M_dict[age]["Berat"][3]

        elif weight > M_dict[age]["Berat"][3]:
            SD = (weight - M_dict[age]["Berat"][2])/(M_dict[age]["Berat"][4] - M_dict[age]["Berat"][3])

        elif weight < M_dict[age]["Berat"][3]:
            SD = (weight-M_dict[age]["Berat"][3])/(M_dict[age]["Berat"][3] - M_dict[age]["Berat"][2])

    elif gender.upper() == "F":
        #Calculating SD based on given constant
        if weight == F_dict[age]["Berat"][3]:
            SD = (weight-F_dict[age]["Berat"][3])/F_dict[age]["Berat"][3]

        elif weight > F_dict[age]["Berat"][3]:
            SD = (weight - F_dict[age]["Berat"][2])/(F_dict[age]["Berat"][4] - F_dict[age]["Berat"][3])

        elif weight < F_dict[age]["Berat"][3]:
            SD = (weight-F_dict[age]["Berat"][3])/(F_dict[age]["Berat"][3] - F_dict[age]["Berat"][2])

    else:
        return {"SD": None, "Status": "Invalid Gender"}

    #Get Status Gizi
    if round(SD) < -3:
        status = "Suspect Stunting" #"Severe Underweight"
    elif round(SD) >= -3 and round(SD) < -2:
        status = "Suspect Stunting" #"Underweight"
    elif round(SD) > -2 and round(SD) <= 2:
        status = "Non Suspect Stunting" #"Normal"
    elif round(SD) > 2:
        status = "Non Suspect Stunting" #"More Nutritions"

    return {"SD": round(SD), "Status": status}
